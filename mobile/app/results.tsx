import { View, Text, StyleSheet, ScrollView, TouchableOpacity, Image, Share } from 'react-native';
import { useLocalSearchParams, useRouter } from 'expo-router';
import { Ionicons } from '@expo/vector-icons';
import { Colors, Spacing } from '@/constants/theme';

function SeverityBadge({ severity, confidence }: { severity: string; confidence: number }) {
  const color = severity === 'Mild' ? Colors.mild
    : severity === 'Moderate' ? Colors.moderate
    : severity === 'Severe' ? Colors.severe
    : Colors.inconclusive;

  return (
    <View style={styles.severityContainer}>
      <View style={[styles.severityBadge, { borderColor: color }]}>
        <Text style={[styles.severityLabel, { color }]}>{severity}</Text>
      </View>
      <Text style={styles.confidenceText}>{confidence.toFixed(1)}% confidence</Text>
    </View>
  );
}

function Section({ title, icon, children }: { title: string; icon: string; children: React.ReactNode }) {
  return (
    <View style={styles.section}>
      <View style={styles.sectionHeader}>
        <Ionicons name={icon as any} size={20} color={Colors.indigo} />
        <Text style={styles.sectionTitle}>{title}</Text>
      </View>
      {children}
    </View>
  );
}

export default function ResultsScreen() {
  const router = useRouter();
  const { data, imageUri } = useLocalSearchParams<{ data: string; imageUri: string }>();

  const results = data ? JSON.parse(data) : null;

  if (!results) {
    return (
      <View style={[styles.container, styles.center]}>
        <Text style={{ color: Colors.textSecondary }}>No results found.</Text>
      </View>
    );
  }

  // Extract from nested primary_diagnosis (Flask API format)
  const severity = results.primary_diagnosis?.severity || results.severity || 'Unknown';
  const confidence = results.primary_diagnosis?.confidence || results.confidence || 0;

  const shareReport = async () => {
    try {
      await Share.share({
        message: `🧴 Acne AI Scan Results\n\nSeverity: ${severity}\nConfidence: ${confidence.toFixed(1)}%\nSkin Type: ${results.skin_type || 'N/A'}\n\nLikely Types: ${results.recommendations?.likely_types?.join(', ') || 'N/A'}\n\nGenerated by Acne AI`,
      });
    } catch (e) {}
  };

  const recs = results.recommendations || {};

  return (
    <View style={styles.container}>
      {/* Top Bar */}
      <View style={styles.topBar}>
        <TouchableOpacity onPress={() => router.back()}>
          <Ionicons name="close" size={28} color={Colors.textSecondary} />
        </TouchableOpacity>
        <Text style={styles.topTitle}>Analysis Results</Text>
        <TouchableOpacity onPress={shareReport}>
          <Ionicons name="share-outline" size={24} color={Colors.indigo} />
        </TouchableOpacity>
      </View>

      <ScrollView contentContainerStyle={styles.content} showsVerticalScrollIndicator={false}>
        {/* Image + Severity */}
        {imageUri && (
          <Image source={{ uri: imageUri }} style={styles.resultImage} />
        )}

        <SeverityBadge severity={severity} confidence={confidence} />

        {/* Likely Types */}
        {recs.likely_types?.length > 0 && (
          <Section title="Likely Acne Types" icon="medical">
            <View style={styles.tagsRow}>
              {recs.likely_types.map((t: string, i: number) => (
                <View key={i} style={styles.tag}>
                  <Text style={styles.tagText}>{t}</Text>
                </View>
              ))}
            </View>
          </Section>
        )}

        {/* Morning Routine */}
        {recs.morning_routine?.length > 0 && (
          <Section title="Morning Routine ☀️" icon="sunny">
            {recs.morning_routine.map((step: string, i: number) => (
              <View key={i} style={styles.routineStep}>
                <Text style={styles.stepNumber}>{i + 1}</Text>
                <Text style={styles.stepText}>{step}</Text>
              </View>
            ))}
          </Section>
        )}

        {/* Night Routine */}
        {recs.night_routine?.length > 0 && (
          <Section title="Night Routine 🌙" icon="moon">
            {recs.night_routine.map((step: string, i: number) => (
              <View key={i} style={styles.routineStep}>
                <Text style={styles.stepNumber}>{i + 1}</Text>
                <Text style={styles.stepText}>{step}</Text>
              </View>
            ))}
          </Section>
        )}

        {/* Key Ingredients */}
        {recs.key_ingredients?.length > 0 && (
          <Section title="Key Ingredients" icon="flask">
            {recs.key_ingredients.map((ing: any, i: number) => (
              <View key={i} style={styles.ingredientCard}>
                <Text style={styles.ingredientName}>{ing.name || ing}</Text>
                {ing.purpose && <Text style={styles.ingredientPurpose}>{ing.purpose}</Text>}
              </View>
            ))}
          </Section>
        )}

        {/* Do's and Don'ts */}
        {(recs.dos?.length > 0 || recs.donts?.length > 0) && (
          <View style={styles.dosRow}>
            {recs.dos?.length > 0 && (
              <View style={[styles.dosCard, { borderColor: Colors.emerald }]}>
                <Text style={[styles.dosTitle, { color: Colors.emerald }]}>✅ Do's</Text>
                {recs.dos.map((d: string, i: number) => (
                  <Text key={i} style={styles.dosItem}>• {d}</Text>
                ))}
              </View>
            )}
            {recs.donts?.length > 0 && (
              <View style={[styles.dosCard, { borderColor: Colors.red }]}>
                <Text style={[styles.dosTitle, { color: Colors.red }]}>❌ Don'ts</Text>
                {recs.donts.map((d: string, i: number) => (
                  <Text key={i} style={styles.dosItem}>• {d}</Text>
                ))}
              </View>
            )}
          </View>
        )}

        {/* Timeline */}
        {recs.timeline && (
          <Section title="Expected Timeline" icon="time">
            <Text style={styles.timelineText}>{recs.timeline}</Text>
          </Section>
        )}

        {/* Disclaimer */}
        <View style={styles.disclaimer}>
          <Ionicons name="information-circle" size={18} color={Colors.textMuted} />
          <Text style={styles.disclaimerText}>
            This is an AI-generated screening tool, not medical advice. Consult a dermatologist for professional diagnosis.
          </Text>
        </View>

        <View style={{ height: 40 }} />
      </ScrollView>
    </View>
  );
}

